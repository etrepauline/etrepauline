


$(document).ready(function () {

    // --------------------------------------
    // --------------------------------------
    //               TOOLBOXES
    // --------------------------------------
    // --------------------------------------


    // tooltip de gauche
    $(".arrow-left").on("mouseenter", function () {
        $(".toolbox-g").removeClass('hidden');
    }).on("mouseleave", function () {
        $(".toolbox-g").addClass('hidden');
    })

    // tooltip de droite
    $(".arrow-right").on("mouseenter", function () {
        $(".toolbox-d").removeClass('hidden');
    }).on("mouseleave", function () {
        $(".toolbox-d").addClass('hidden');
    })

    // --------------------------------------
    // --------------------------------------
    //      Hover images (index.html)
    // --------------------------------------
    // --------------------------------------


})






